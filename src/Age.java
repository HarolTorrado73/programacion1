public class Age {
    public static void main(String[] args) {
        // Declaración y asignación de variables
        byte edad = 25; // Edad en años
        double altura = 1.75; // Altura en metros
        char inicialNombre = 'A'; // Inicial del nombre
        boolean esMayorDeEdad = true; // Mayoría de edad (true para mayor de 18)

        // Imprimir los valores de las variables
        System.out.println("Edad: " + edad);
        System.out.println("Altura: " + altura + " metros");
        System.out.println("Inicial del nombre: " + inicialNombre);
        System.out.println("Mayor de edad: " + esMayorDeEdad);
    }
}
