public class Condiciones {
    public static void main(String[] args) {
        int i = 0;
    while (i < 5) {  //hasta que sea mayor a 5
    System.out.println("i es " + i);
    i++;
        }

    int y = 0;
    do {
        System.out.println("i es " + y);
        y++;
        } while (y < 5);
    }
}
